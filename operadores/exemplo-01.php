<?php
//////////////////// Operadores de atribuição//////////////////////////


$nome = "Caio";	// Atribuição (=), operador de string que atribui um valor a uma variável, "valor entre áspas".

echo $nome . ", este é meu nome!";	// Concatenador (.), operador de string que junta duas strings.
echo "<br>";

$nome .= " Benevides";	// Composto (.=), atribui a variável o valor anterior, acrescentando o novo valor atribuído.
echo $nome;
echo "<br>";



?>