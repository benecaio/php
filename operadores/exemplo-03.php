<?php
/////////////////Operadores aritméticos///////////////////

$a = 80;
$b = 20;

echo "Essa é a variável a: ". $a;
echo "<br>";

echo "Essa é a variável b: " . $b;
echo "<br>";

echo $a + $b;	// Soma.
echo "<br>";

echo $a - $b;	// Subtração.
echo "<br>";

echo $a * $b;	// Multiplicação.
echo "<br>";

echo $a / $b;	// Divisão.
echo "<br>";

echo $a % $b;	// Módulo. Resto da divisão inteira mais próxima.
echo "<br>";
echo 7 % 2;	// 7 dividido por 2 = 3,5. Sendo assim o número inteiro mais próximo = 3. O resto dessa divisão é 1. 
echo "<br>";


$a= 8;
$b= 2;

echo $a ** $b;	// Potência, a primeira variável é a base e a segunda é o expoente.
echo "<br>";


?>